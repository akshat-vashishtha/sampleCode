	public void putRequiredData(String mainresponse)
	{
		
		JSONObject enquiryResponse=null;
		try {
			enquiryResponse = new JSONObject(mainresponse);
			JSONArray addressesjson= enquiryResponse.getJSONObject("reportdata").getJSONObject("idandcontactinfo").getJSONArray("addressinfo");
	    	setAttribute("addressinfo", addressesjson);
	    	JSONObject personalInfo= enquiryResponse.getJSONObject("reportdata").getJSONObject("idandcontactinfo").getJSONObject("personalinfo");
	    	setAttribute("personalInfo", personalInfo);
	    	JSONObject identityinfo= enquiryResponse.getJSONObject("reportdata").getJSONObject("idandcontactinfo").getJSONObject("identityinfo");
	    	setAttribute("identityinfo", identityinfo);
	    	JSONObject nsdldata=enquiryResponse.getJSONObject("reportdata").getJSONObject("verifyidresponse").getJSONObject("vidnsdlresponses");
	    	setAttribute("nsdldata", nsdldata);
	    	JSONObject voterdata=enquiryResponse.getJSONObject("reportdata").getJSONObject("verifyidresponse").getJSONObject("vidvoterresponses");
	    	setAttribute("voterdata", voterdata);
	    	String disclaimer=enquiryResponse.getJSONObject("reportdata").get("disclaimer")+"";
	    	setAttribute("disclaimer", disclaimer);
	    	setAttribute("responsetransaction", enquiryResponse.get("inquiryresponseheader")+"");	
		} catch (JSONException e) {
			logger.error("exception in JsonObject:: "+e.getMessage());
		}
		
	}
	
	public ConvertEquifaxDataToReportsHTML(String mainresponse) {
		lastData = new HashMap();
		putRequiredData(mainresponse);
	}

	public Object getAttribute(String key) {
		Object returnObj = new Object();
		try {
			returnObj = lastData.get(key);
		} catch (Exception ec) {
			logger.error("the given attribute of converting data to html" + ec);
		}
		return returnObj;
	}

	public void setAttribute(String key,Object value) {
		lastData.put(key, value);
	}
	
	
	
